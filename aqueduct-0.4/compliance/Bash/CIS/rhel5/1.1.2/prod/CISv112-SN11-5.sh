#11.5 Ensure perms on man and doc pages prevent modification by unprivileged users
#Description:
#Ensure permissions on man pages and system documentation files prevent modification 
#by unprivledged users. These are stable files, provided as a resource as part the 
#Operating System, and in some cases by a particular package creator/developer, 
#that users have no need for more than read access to.

chmod -R go-w /usr/share/doc 
chmod -R go-w /usr/local/share/doc

chmod -R go-w /usr/share/man 
chmod -R go-w /usr/local/share/man